//Java program to print defalut values of data types
public class defaultvalues {
		static int Int;
		static float Float;
		static double Double;
		static boolean Boolean;
		static long Long;
		static short Short;
		static char Char;
		static byte Byte;
		public static void main(String[] args){
			System.out.println("default value of int  is :" + Int);
			System.out.println("default value of float is : " + Float);
			System.out.println("default value of double is : " + Double);
			System.out.println("default value of boolean is : " + Boolean);	
			System.out.println("default value of long is : " + Long);	
			System.out.println("default value of short is : " + Short);
			System.out.println("default value of char is : " + Char);
			System.out.println("default value of byte is : " + Byte);	
																
	}
}
